    private void fiyatdegistirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fiyatdegistirActionPerformed
        
        
        
        DefaultTableModel model = (DefaultTableModel) uruntablosu.getModel();
        
        if(uruntablosu.getSelectedRow() != -1){
            String yeni = JOptionPane.showInputDialog("Yeni Fiyati giriniz");
        
            model.setValueAt(yeni,uruntablosu.getSelectedRow(),1);
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Bir Ürün Seçiniz " , "Uyarı !" , JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_fiyatdegistirActionPerformed

