    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
        
        DefaultTableModel model = (DefaultTableModel) uruntablosu.getModel();
        String eklenecek = degistirilecek_isim.getText();
        String fiyat = degistirilecek_fiyat.getText();
        String adet = degistirilecek_miktar.getText();
        if (degistirilecek_isim.getText().trim().equals("")||degistirilecek_isim.getText().trim().equals("isim")){
            JOptionPane.showMessageDialog(null, "Lütfen Ürün İsmini Giriniz", "Uyarı" , JOptionPane.WARNING_MESSAGE);
            
        }
        else{
            if (degistirilecek_fiyat.getText().trim().equals("")||degistirilecek_fiyat.getText().trim().equals("Fiyat")){
                JOptionPane.showMessageDialog(null, "Lütfen Fiyat Giriniz", "Uyarı" , JOptionPane.WARNING_MESSAGE);
                
            }
            else{
                if (degistirilecek_miktar.getText().trim().equals("")||degistirilecek_miktar.getText().trim().equals("Miktar")){
                    JOptionPane.showMessageDialog(null, "Lütfen Adet Giriniz", "Uyarı" , JOptionPane.WARNING_MESSAGE);
                    
                }
                else{
                    Object[] eklelan = {eklenecek,fiyat,adet};
                    model.addRow(eklelan);
                            
                }
            }
        }
    }//GEN-LAST:event_ekleActionPerformed

